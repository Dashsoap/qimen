export const useQimenInfoStore = defineStore('qimenInfo', {
  state: () => ({
    selectedPalace: null,
    displayName: null,
    showMeaning: false,
    currentMeaning: '',
    palaceMeanings
  }),
  
  actions: {
    showPalaceMeaning(palaceName, customDisplayName = null) {
      this.selectedPalace = palaceName;
      this.displayName = customDisplayName || palaceName;
      this.currentMeaning = this.palaceMeanings[palaceName] || '未找到解释';
      this.showMeaning = true;
    },
    
    hideMeaning() {
      this.showMeaning = false;
    },
    
    getMeaning(key) {
      return this.palaceMeanings[key] || '未找到解释';
    },

    // 获取宫位的完整解释
    getFullPalaceExplanation(palaceName) {
      const palaceInfo = this.palaceMeanings[palaceName] || '未找到解释';
      
      // 这里我们可以获取宫位中的所有元素并添加它们的解释
      // 但这需要访问当前排盘的数据
      
      return palaceInfo;
    },

    // 显示宫位信息，支持HTML内容
    showPalaceInfo(palaceName, htmlContent) {
      this.selectedPalace = palaceName;
      this.displayName = palaceName;
      this.currentMeaning = htmlContent;
      this.showMeaning = true;
      this.isHtmlContent = true; // 添加标记，表示内容包含HTML
    }
  }
}); 